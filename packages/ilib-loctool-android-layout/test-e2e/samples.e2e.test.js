const path = require("path");
const fs = require("fs");
const { expectFileToMatchSnapshot, LoctoolRunner } = require("ilib-internal");

describe("samples", () => {
    describe("android", () => {
        const projectPath = path.resolve(__dirname, "..", "samples", "android");
        const xliffPath = path.resolve(projectPath, "sample-extracted.xliff");

        beforeAll(async () => {
            const loctool = new LoctoolRunner(projectPath);
            await loctool.run("localize");
        });

        afterAll(() => {
            // Clean up all files generated by loctool localize
            const filesToClean = [
                xliffPath, // sample-extracted.xliff
                path.resolve(projectPath, "sample-new-ko-KR.xliff"),
                path.resolve(projectPath, "sample-new-ru-RU.xliff"),
                path.resolve(projectPath, "res/values/strings-auto.xml")
            ];

            filesToClean.forEach(file => {
                if (fs.existsSync(file)) {
                    fs.unlinkSync(file);
                }
            });

            // Clean up generated directories
            const dirsToClean = [
                path.resolve(projectPath, "res/values-ko"),
                path.resolve(projectPath, "res/values-ru")
            ];

            dirsToClean.forEach(dir => {
                if (fs.existsSync(dir)) {
                    fs.rmSync(dir, { recursive: true, force: true });
                }
            });
        });

        it("should produce an extracted XLIFF file", () => {
            expectFileToMatchSnapshot(xliffPath);
        });
    });
});
